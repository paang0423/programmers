def solution(h1, m1, s1, h2, m2, s2):
    answer = 0
    start = h1 * 3600 + m1 * 60 + s1  # 시작 시간을 초 단위로 변환
    end = h2 * 3600 + m2 * 60 + s2    # 끝 시간을 초 단위로 변환
    
    # 초침이 시침/분침과 겹치는 횟수를 계산
    answer += int(end / 3600 * 59)  # 시침과 초침이 겹치는 주기를 기반으로 계산
    answer -= start // (3600 / 59)  # 시작 시간에서 해당 주기의 겹치는 횟수를 빼줌
    
    answer += end // (360 * 120 / 719)  # 분침과 초침이 겹치는 주기를 기반으로 계산
    answer -= start // (360 * 120 / 719)  # 시작 시간에서 해당 주기의 겹치는 횟수를 빼줌
    
    # 12시간이 넘어갈 경우, 알람이 울리는 횟수를 조정
    if start <= 12 * 60 * 60 <= end:  # 12시 정각에 걸리는지 확인
        answer -= 1  # 이미 포함된 알람을 중복 계산하지 않도록 조정
    
    # 시작 시간이 0시 정각이나 12시 정각이면, 한 번 더 알람을 추가
    if start == 0 or start == 12 * 60 * 60:
        answer += 1

    return answer
